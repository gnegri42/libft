#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0')
		i++;
	while (s[i] != s[0])
	{
		if (c == s[i])
			return ((char *)&s[i]);
		i--;
	}
	if (c == s[i])
		return ((char *)&s[i]);
	return (0);
}
